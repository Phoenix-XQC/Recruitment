CREATE TABLE work(
id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
work VARCHAR(64),
location VARCHAR(64),
low INTEGER,
high INTEGER,
jobtime DATE,
effective INTEGER,
company VARCHAR(64),
experence VARCHAR(64),
type VARCHAR(64),
profile VARCHAR(64)
);


CREATE TABLE message(
id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
bio_id INTEGER,
company VARCHAR(64),
message VARCHAR(1024)
);

CREATE TABLE collection(
userid INTEGER,
jobid INTEGER
);

CREATE TABLE bio(
id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
job VARCHAR(64),
location VARCHAR(64),
low INTEGER,
high INTEGER,
