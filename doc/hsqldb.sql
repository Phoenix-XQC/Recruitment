CREATE TABLE USERS(
id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
username VARCHAR(256),
password VARCHAR(256),
email VARCHAR(64),
phone VARCHAR(64),
sex VARCHAR(64),
profile VARCHAR(256)
);

CREATE TABLE company(
id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
company_name VARCHAR(256),
username VARCHAR(256),
password VARCHAR(256),
profile VARCHAR(256),
location VARCHAR(64),
phone VARCHAR(64),
email VARCHAR(64)
);


SELECT ID, COMPANY_NAME, USERNAME, PASSWORD, PROFILE, LOCATION, PHONE, EMAIL
FROM PUBLIC.COMPANY;


DROP TABLE bio;




CREATE TABLE bio(
id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
name VARCHAR(64),
phone VARCHAR(64),
school VARCHAR(64),
email VARCHAR(64),
sex VARCHAR(16),
TYPE VARCHAR(64),
workTime DATE,
job VARCHAR(64),
location VARCHAR(64),
low INTEGER,
high INTEGER,
profile VARCHAR(5096)
);
