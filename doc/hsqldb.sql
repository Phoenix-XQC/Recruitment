CREATE TABLE USERS(
id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
username VARCHAR(256),
password VARCHAR(256),
email VARCHAR(64),
phone VARCHAR(64),
sex BOOLEAN,
profile VARCHAR(256)
);

CREATE TABLE company(
id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
company_name VARCHAR(256),
username VARCHAR(256),
password VARCHAR(256),
profile VARCHAR(256),
location VARCHAR(64),
phone VARCHAR(64),
email VARCHAR(64)
);


